package com.qust.exam.entity;

public class Choice {
private int qid;
private int seq;
private String  qcont;
private  int score;
public int getQid() {
	return qid;
}
public void setQid(int qid) {
	this.qid = qid;
}
public int getSeq() {
	return seq;
}
public void setSeq(int seq) {
	this.seq = seq;
}
public String getQcont() {
	return qcont;
}
public void setQcont(String qcont) {
	this.qcont = qcont;
}
public int getScore() {
	return score;
}
public void setScore(int score) {
	this.score = score;
}

}
